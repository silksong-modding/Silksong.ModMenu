namespace Silksong.ModMenu.Elements;

/// <summary>
/// Font size 'groups' respected by all first-class menu element implementations.
/// </summary>
public enum FontSizes
{
    /// <summary>
    /// Smaller font sizes for more compact menus.
    /// </summary>
    Small,

    /// <summary>
    /// Standard font sizes seen throughout most of the menu.
    /// </summary>
    Medium,

    /// <summary>
    /// Larger font sizes for menus with egos.
    /// </summary>
    Large,
}
